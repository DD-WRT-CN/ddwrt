
// Zahlen summieren, Java Version

public class suml {
	
			
	static public void main(String [] arg) {

		long s1 = 0, s2 = 0;
		long i = Integer.parseInt (arg[0]);

		
		for (; i > 0; i--) {
			s1++;
			s2--;
			}
		
		System.out.print (".... done, sum: ");
		System.out.print   (s1);
		System.out.println (s2);
		}
		
		
	}
	